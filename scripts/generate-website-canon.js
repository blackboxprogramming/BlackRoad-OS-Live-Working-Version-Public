import { execFileSync } from 'child_process';
import { mkdir, readFile, writeFile } from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

import { PRODUCTS } from '../src/blackroad/products.js';
import { SITES } from '../src/blackroad/sites.js';
import {
  AGENTS,
  AGENT_ALIASES,
  PRODUCT_ALIASES,
  SYSTEM_COMMANDS,
} from '../src/blackroad/command/commandRegistry.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'website', 'src', 'generated', 'canon.js');

const DOC_PATHS = [
  'AGENT-ENTRY.md',
  'docs/COMMAND_DOCK_ROUTING.md',
  'PRODUCTS.md',
  'FILESYSTEM.md',
  'FLEET-STATUS.md',
  'MEMORY.md',
  'COLLAB.md',
  'CODEX.md',
  'BRTODO.md',
  'docs/BlackRoad-Index.md.rtf',
];

function productAliasesById() {
  const aliases = new Map();
  for (const [alias, id] of Object.entries(PRODUCT_ALIASES)) {
    if (!aliases.has(id)) aliases.set(id, []);
    aliases.get(id).push(alias);
  }
  return aliases;
}

function agentAliasesById() {
  const aliases = new Map();
  for (const [alias, id] of Object.entries(AGENT_ALIASES)) {
    if (!aliases.has(id)) aliases.set(id, []);
    aliases.get(id).push(alias);
  }
  return aliases;
}

function toProductRoute(product, aliases) {
  return {
    id: product.id,
    name: product.name,
    domain: product.domain,
    url: `https://${product.domain}`,
    route: product.route,
    kind: 'product',
    description: product.oneLine,
    tags: product.tags,
    aliases: [...new Set([product.name.toLowerCase(), product.domain, ...(aliases.get(product.id) || [])])],
  };
}

function toSiteRoute(site) {
  return {
    id: site.id,
    name: site.title,
    domain: site.domain,
    url: `https://${site.domain}`,
    kind: 'site',
    description: site.purpose,
    tags: [site.type, ...(site.connectedAgents || []).filter(Boolean)],
    aliases: [...new Set([site.domain, site.title.toLowerCase()])],
  };
}

function toAgentEntry(agent, aliases) {
  return {
    id: agent.id,
    name: agent.name,
    role: agent.role,
    product: null,
    domain: 'blackroad.me',
    url: `https://blackroad.me/${agent.id}`,
    description: agent.role,
    tags: ['agent'],
    aliases: [...new Set([agent.id, agent.name.toLowerCase(), ...(aliases.get(agent.id) || [])])],
  };
}

function toSystemEntry([command, config]) {
  return {
    id: config.surfaceId,
    name: config.label,
    command,
    url: config.url,
    kind: 'system',
    description: `${config.label} system surface.`,
    tags: ['system'],
    aliases: [command],
  };
}

async function extractDocEntry(relativePath) {
  const absolutePath = path.join(repoRoot, relativePath);
  if (relativePath.endsWith('.rtf')) {
    return {
      id: path.basename(relativePath, path.extname(relativePath)).toLowerCase().replaceAll(/[^a-z0-9]+/g, '-'),
      title: 'Institutional Index',
      path: relativePath,
      kind: 'doc',
      description: 'Canonical BlackRoad institutional index.',
      summary: 'Indexed canon for identity, schools, divisions, products, and governance.',
      tags: ['doc', 'index', 'canon'],
      aliases: ['index', 'institutional index'],
    };
  }

  const content = await readFile(absolutePath, 'utf8');
  const lines = content.split(/\r?\n/);
  const heading = lines.find((line) => line.trim().startsWith('#')) || path.basename(relativePath);
  const title = heading.replace(/^#+\s*/, '').trim();
  const summary =
    lines.find((line) => {
      const value = line.trim();
      return value && !value.startsWith('#') && !value.startsWith('>') && !value.startsWith('|');
    }) || `Generated from ${relativePath}`;

  return {
    id: path.basename(relativePath, path.extname(relativePath)).toLowerCase().replaceAll(/[^a-z0-9]+/g, '-'),
    title,
    path: relativePath,
    kind: 'doc',
    description: summary,
    summary,
    tags: ['doc'],
    aliases: [title.toLowerCase()],
  };
}

function extractSections() {
  const indexPath = path.join(repoRoot, 'docs', 'BlackRoad-Index.md.rtf');
  const text = execFileSync('textutil', ['-convert', 'txt', '-stdout', indexPath], {
    encoding: 'utf8',
  });
  const entries = [];
  for (const line of text.split(/\r?\n/)) {
    const match = line.match(/^\s*(?:•\s*)?(\d+)\.\s+(.+?)\s*$/);
    if (!match) continue;
    const number = Number(match[1]);
    const title = match[2].trim();
    entries.push({
      id: `section-${number}`,
      number,
      title,
      kind: 'section',
      description: 'Institutional index section.',
      tags: ['index', 'section'],
      aliases: [String(number), title.toLowerCase()],
    });
  }
  return entries;
}

function formatModule(data) {
  return `// Generated by scripts/generate-website-canon.js\n` +
    `// Do not edit by hand. Update shared canon or the generator.\n\n` +
    `export const PRODUCT_ROUTES = ${JSON.stringify(data.productRoutes, null, 2)};\n\n` +
    `export const SITE_ROUTES = ${JSON.stringify(data.siteRoutes, null, 2)};\n\n` +
    `export const AGENT_ENTRIES = ${JSON.stringify(data.agentEntries, null, 2)};\n\n` +
    `export const SYSTEM_ENTRIES = ${JSON.stringify(data.systemEntries, null, 2)};\n\n` +
    `export const DOC_ENTRIES = ${JSON.stringify(data.docEntries, null, 2)};\n\n` +
    `export const SECTION_ENTRIES = ${JSON.stringify(data.sectionEntries, null, 2)};\n`;
}

async function main() {
  const productAliases = productAliasesById();
  const agentAliases = agentAliasesById();

  const productRoutes = PRODUCTS.map((product) => toProductRoute(product, productAliases));
  const siteRoutes = SITES.map((site) => toSiteRoute(site));
  const agentEntries = AGENTS.map((agent) => toAgentEntry(agent, agentAliases));
  const systemEntries = Object.entries(SYSTEM_COMMANDS).map(toSystemEntry);
  const docEntries = await Promise.all(DOC_PATHS.map(extractDocEntry));
  const sectionEntries = extractSections();

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(
    outputPath,
    formatModule({
      productRoutes,
      siteRoutes,
      agentEntries,
      systemEntries,
      docEntries,
      sectionEntries,
    }),
    'utf8'
  );

  console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});

